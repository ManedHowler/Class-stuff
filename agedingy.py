# This program calculates your age in the year 2050.
# Input:  None
# Output: Your current age followed by your age in 2050

# Create your variables here

# insert current age
myCurrentAge = 20

# insert current year
currentYear = 2024

# Calculate age in 2050
myNewAge = myCurrentAge + (2050 - currentYear)
